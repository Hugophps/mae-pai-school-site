const supportList = document.getElementById('support-list');
const langButtons = {
  fr: document.getElementById('lang-fr'),
  en: document.getElementById('lang-en')
};

function getByPath(obj, path) {
  return path.split('.').reduce((acc, key) => (acc ? acc[key] : undefined), obj);
}

function renderSupportCards(cards) {
  supportList.innerHTML = cards
    .map(
      (card) =>
        `<article class="action-item"><h3>${card.title}</h3><p>${card.body}</p></article>`
    )
    .join('');
}

function setLanguage(lang) {
  document.documentElement.lang = lang;

  document.querySelectorAll('[data-i18n]').forEach((node) => {
    const key = node.getAttribute('data-i18n');
    const value = getByPath(dictionary[lang], key);
    if (typeof value === 'string') {
      node.textContent = value;
    }
  });

  renderSupportCards(dictionary[lang].support.cards);

  Object.entries(langButtons).forEach(([key, button]) => {
    button.classList.toggle('active', key === lang);
  });

  localStorage.setItem('maepai-lang', lang);
}

langButtons.fr.addEventListener('click', () => setLanguage('fr'));
langButtons.en.addEventListener('click', () => setLanguage('en'));

const preferred = localStorage.getItem('maepai-lang');
setLanguage(preferred === 'en' ? 'en' : 'fr');
